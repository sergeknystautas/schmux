#!/bin/bash
set -e

# Git requires user config for schmux repo operations
git config --global user.email "test@schmux.dev"
git config --global user.name "Schmux Test"

# Start Xvfb for remote clipboard paste tests (provides X11 clipboard via xclip)
Xvfb :99 -screen 0 1x1x8 &>/dev/null &
export DISPLAY=:99

# Set up frontend coverage collection directory if coverage is enabled
if [ -n "${GOCOVERDIR:-}" ]; then
    export COVERAGE_DIR=/tmp/fe-coverage
    mkdir -p "$COVERAGE_DIR"
fi

# Pass through worker count (defaults to nproc)
export TEST_WORKERS="${TEST_WORKERS:-$(nproc)}"

# Run Playwright scenario tests
# Each worker starts its own isolated daemon via the worker-scoped fixture.
cd /app/test/scenarios/generated
set +e
PLAYWRIGHT_ARGS=()
if [ -n "${TEST_GREP:-}" ]; then
    PLAYWRIGHT_ARGS+=(--grep "$TEST_GREP")
fi
if [ -n "${TEST_REPEAT:-}" ]; then
    PLAYWRIGHT_ARGS+=(--repeat-each "$TEST_REPEAT")
fi
npx playwright test "${PLAYWRIGHT_ARGS[@]}"
TEST_EXIT=$?
set -e

# Copy test artifacts (videos, screenshots, traces, report) to mounted volume
if [ -d /artifacts ]; then
    echo "Copying test artifacts to /artifacts..."
    # test-results contains per-test videos, screenshots, traces (on failure)
    if [ -d test-results ]; then
        cp -r test-results /artifacts/test-results
    fi
    # playwright-report is always generated by the html reporter
    if [ -d playwright-report ]; then
        cp -r playwright-report /artifacts/playwright-report
    fi
    # Copy terminal fidelity diagnostics if any failures wrote them
    if [ -d /tmp/terminal-diagnostics ] && [ "$(ls -A /tmp/terminal-diagnostics 2>/dev/null)" ]; then
        echo "Copying terminal diagnostics to /artifacts/terminal-diagnostics..."
        cp -r /tmp/terminal-diagnostics /artifacts/terminal-diagnostics
    fi
    # Copy frontend coverage data if collected
    if [ -d "$COVERAGE_DIR" ] && [ "$(ls -A "$COVERAGE_DIR" 2>/dev/null)" ]; then
        echo "Copying frontend coverage data to /artifacts/fe-coverage..."
        mkdir -p /artifacts/fe-coverage
        cp "$COVERAGE_DIR"/*.json /artifacts/fe-coverage/
    fi
fi

exit $TEST_EXIT
